#include "sdram.h"
/**
  * @brief  SDRAM初始化序列
  * @param  hsdram: SDRAM句柄指针
  * @param  Command: FMC命令结构体指针
  * @retval None
  */
void SDRAM_Initialization_Sequence(SDRAM_HandleTypeDef *hsdram, FMC_SDRAM_CommandTypeDef *Command)
{
    volatile uint32_t tmpmrd = 0;

    /* Step 1: 时钟配置使能命令 */
    Command->CommandMode             = FMC_SDRAM_CMD_CLK_ENABLE;    // Enable SDRAM clock
    Command->CommandTarget           = FMC_COMMAND_TARGET_BANK;     // Target bank selection
    Command->AutoRefreshNumber       = 1;
    Command->ModeRegisterDefinition  = 0;

    HAL_SDRAM_SendCommand(hsdram, Command, SDRAM_TIMEOUT);         // Send command
    HAL_Delay(1);                                                  // Wait at least 100us (实际延迟1ms更安全)

    /* Step 2: 预充电所有bank命令 */ 
    Command->CommandMode             = FMC_SDRAM_CMD_PALL;          // Precharge all banks
    Command->CommandTarget           = FMC_COMMAND_TARGET_BANK;     // Target bank selection
    Command->AutoRefreshNumber       = 1;
    Command->ModeRegisterDefinition  = 0;

    HAL_SDRAM_SendCommand(hsdram, Command, SDRAM_TIMEOUT);         // Send command

    /* Step 3: 自动刷新命令 (建议至少2次，此处设置为8次更可靠) */ 
    Command->CommandMode             = FMC_SDRAM_CMD_AUTOREFRESH_MODE; // Enable auto-refresh
    Command->CommandTarget           = FMC_COMMAND_TARGET_BANK;        // Target bank selection
    Command->AutoRefreshNumber       = 8;                              // Number of auto-refresh cycles
    Command->ModeRegisterDefinition  = 0;

    HAL_SDRAM_SendCommand(hsdram, Command, SDRAM_TIMEOUT);         // Send command

    /* Step 4: 配置模式寄存器 */
    tmpmrd = (uint32_t)SDRAM_MODEREG_BURST_LENGTH_2          |     // Burst length = 2
             SDRAM_MODEREG_BURST_TYPE_SEQUENTIAL             |     // Sequential burst
             SDRAM_MODEREG_CAS_LATENCY_3                     |     // CAS latency = 3
             SDRAM_MODEREG_OPERATING_MODE_STANDARD           |     // Standard operation
             SDRAM_MODEREG_WRITEBURST_MODE_SINGLE;                // Single write burst

    Command->CommandMode             = FMC_SDRAM_CMD_LOAD_MODE;    // Load mode register
    Command->CommandTarget           = FMC_COMMAND_TARGET_BANK;    // Target bank selection
    Command->AutoRefreshNumber       = 1;
    Command->ModeRegisterDefinition  = tmpmrd;

    HAL_SDRAM_SendCommand(hsdram, Command, SDRAM_TIMEOUT);         // Send command
    
    /* Step 5: 设置刷新速率 */
    // 对于64ms刷新周期和4096行: 
    // 刷新计数 = (刷新周期 * 时钟频率) / 行数 - 20
    // 示例值918对应100MHz时钟
    HAL_SDRAM_ProgramRefreshRate(hsdram, 918);                     // Set refresh rate
}


